    private void deleteUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteUserActionPerformed
        if(usersTable.getSelectedRow() != -1){
            int resposta = JOptionPane.showConfirmDialog(this, "De certeza que pretende remover este utilizador?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(resposta == JOptionPane.YES_OPTION) {
                String id = (String) usersTable.getValueAt(usersTable.getSelectedRow(), 0);
                boolean resultado = d.removeUser(id);
                    if (resultado=true) {
                    JOptionPane.showMessageDialog(this, "Utilizador removido com sucesso!!");
                    TrocarMenu(1);
                }
            }
              
        }else{
           JOptionPane.showMessageDialog(this, "Selecione um Utilizador primeiro!!");
        }   
    }//GEN-LAST:event_deleteUserActionPerformed

