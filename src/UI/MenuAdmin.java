    private void jButton5MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MousePressed

        if(this.registo_email_input.getText().isEmpty() || this.registo_password_input.getText().isEmpty()|| this.registo_nome_input.getText().isEmpty()|| this.registo_telemovel_input.getText().isEmpty()){
            this.errorRegistoDono.setText("Existem campos por preencher");
            this.errorRegistoDono.setVisible(true);
        }else{
            String email = this.registo_email_input.getText();
            String password = this.registo_password_input.getText();
            String nome = this.registo_nome_input.getText();
            String telemovel = this.registo_telemovel_input.getText();

            int status = a.addDono(email, password, nome, telemovel);
            //System.out.println(status);

            switch (status) {
                case 1:
                this.errorRegistoDono.setText("Email já em uso!");
                this.errorRegistoDono.setVisible(true);
                break;
                case 2:this.errorRegistoDono.setText("Email Inválido!");
                this.errorRegistoDono.setVisible(true);
                break;
                case 3:
                this.errorRegistoDono.setText("Numero de Telemovel Inválido!");
                this.errorRegistoDono.setVisible(true);
                break;
                case 0:
                TrocarMenu(2);
                break;
                default:
                throw new AssertionError();
            }
        }
    }//GEN-LAST:event_jButton5MousePressed

