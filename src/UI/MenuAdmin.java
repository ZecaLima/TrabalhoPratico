    private void initAdminTable(){
        ArrayList<Utilizador> users = d.getAllUsers();
        ArrayList<Utilizador> admins = new ArrayList<>();
        
        for(Utilizador aux: users){
            if(aux instanceof Admin){
                admins.add(aux);
            }
        }
        
        DefaultTableModel  adminTableModel = (DefaultTableModel) adminTable.getModel();
        adminTableModel.setRowCount(0);
        for(int i = 0; i<admins.size(); i++){
            adminTableModel.addRow(new Object[]{
                admins.get(i).getId(),
                admins.get(i).getEmail(),
                admins.get(i).getPassword(),
            });           
        }
    }
    
    
    private void TrocarMenu(int num){
        UsersPanel.setVisible(false);
        DonosPanel.setVisible(false);
        AdminsPanel.setVisible(false);
        addDonoPanel.setVisible(false);
        addAdminPanel.setVisible(false);
        
        errorRegistoDono.setVisible(false);
        errorRegistoAdmin.setVisible(false);
        
        switch(num) {
            case 1: UsersPanel.setVisible(true);initUsersTable(); break;
            case 2: DonosPanel.setVisible(true); initDonosTable(); break;
            case 3: AdminsPanel.setVisible(true); initAdminTable(); break;
            case 4: addDonoPanel.setVisible(true); break;
            case 5: addAdminPanel.setVisible(true); break;
            default: break;
        }
    }

